/* 3.WAP to Find Area And Circumference of Circle.
Area of circle is πr2.
Circumference of the circle is 2πr.*/


# include<stdio.h>
int main(){
    float radius;

    printf("enter radius: ");
    scanf("%f", &radius); 

    printf("\n area of the circle is : %.2f", 3.14*radius*radius);
    printf("\n circumference of the circle is : %.2f", 2*3.14*radius);
    
    return 0;
}
    
    
    
    
    
    

    
    
    
    
